// Example of using package and import
// This class would be inside a package, e.g., package mypackage;

import java.util.ArrayList;

public class PackagesAndImportsExamples {

    public static void run() {
        // Using ArrayList from java.util package
        ArrayList<String> fruits = new ArrayList<>();
        fruits.add("Apple");
        fruits.add("Banana");
        fruits.add("Orange");

        System.out.println("Fruits: " + fruits);
    }
}
